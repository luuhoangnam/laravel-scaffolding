<?php

namespace App;

use Illuminate\Database\Eloquent\Model;

/**
 * Class Setting
 * @package App
 */
class Setting extends Model
{

    //--------------------------------------------------------------------------
    // Configurations
    //--------------------------------------------------------------------------
    protected $fillable = ['key', 'value'];
    protected $casts = ['value' => 'object'];

    //--------------------------------------------------------------------------
    // Accessors & Mutators
    //--------------------------------------------------------------------------


    //--------------------------------------------------------------------------
    // Relationships
    //--------------------------------------------------------------------------


    //--------------------------------------------------------------------------
    // Scopes
    //--------------------------------------------------------------------------


    //--------------------------------------------------------------------------
    // Custom Pivot Table
    //--------------------------------------------------------------------------


    //--------------------------------------------------------------------------
    // Helpers
    //--------------------------------------------------------------------------
    /**
     * @param string $key
     * @param array  $columns
     *
     * @return Model|null
     */
    public static function findByKey($key, $columns = ['*'])
    {
        return static::whereKey($key)->first($columns);
    }

}
